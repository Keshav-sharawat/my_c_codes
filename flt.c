#include <stdio.h>
int main()
{
   int i = 1, n;
   printf("\n Hello please enter the number = ");
   scanf("%d", &n);
   /*
   going to start the syntax of for loop and to learn increment operations like i++ and ++i etc.
   it like for better understanding
   */

   printf("\n The series goes: \n");
   for (i = 1; i <= n; i++)
   {

      printf("%d\n", i);
   }
   while (n > 0)
      ;
   return 0;
}